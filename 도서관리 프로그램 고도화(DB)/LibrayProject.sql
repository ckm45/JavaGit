CREATE OR REPLACE TRIGGER CALC_AGE
BEFORE INSERT OR UPDATE ON MEMBER
FOR EACH ROW
BEGIN
    :NEW.AGE := TRUNC(MONTHS_BETWEEN(SYSDATE, :NEW.BIRTHDAY) / 12);
END;
